print('a sequence from 0 to 2')
for i in range(3):
    print(i)
    
print('----------------------')    
print('a sequence from 2 to 4')
for i in range(2, 5):
    print(i)

print('----------------------')    
print('a sequence from 2 to 8 with a step of 2')
for i in range(2, 9, 2):
    print(i)
    
